    private void loadData(Consulta consulta) throws SQLException {

        DefaultTableModel tm = (DefaultTableModel) tbl_pass.getModel();

        while (consulta.getResultado().next()) {
            Object[] rowData = new Object[2];
            
            rowData[0] = consulta.getResultado().getInt(1);
            rowData[1] = consulta.getResultado().getString(2);

            tm.addRow(rowData);
        }
    }

